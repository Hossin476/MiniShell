#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dest;
	if (dest == src)
		return (dest);
	while (n-- > 0)
		*d++ = *s++;
	return (dest);
}
